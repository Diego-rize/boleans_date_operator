//Operadores de comparacion

// == iguales solo en valor: Equivalentes
// === igual en valor y tipos de datos: son exactamente iguales
// != no es igual o diferente a 
// > mayor que
// < menor que
// >= mayor o igual que
// <= menor o igual que


console.log(3 > 2) // True
console.log(3 >= 2) // True
console.log(3 < 2) // False
console.log(2 < 3) // True
console.log(2 <= 3) // True
console.log(3 == 2) // False
console.log(3 != 2) // True
console.log(3 === '3') // False
